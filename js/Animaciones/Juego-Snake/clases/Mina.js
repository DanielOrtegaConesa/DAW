class Mina {
    constructor(x, y) {
        this.posicionX = x;
        this.posicionY = y;
    }

    setPosicionX(x) {
        this.posicionX = x;
    }

    setPosicionY(y) {
        this.posicionY = y;
    }

    getPosicionX() {
        return this.posicionX;
    }

    getPosicionY() {
        return this.posicionY;
    }


}